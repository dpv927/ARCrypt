#include "aes128.h"
#include <stdint.h>
#include <stdio.h>

void printBlock(const uint8_t b[16]) {
  printf("%0x, %0x, %0x, %0x\n", b[0], b[4], b[8], b[12]);
  printf("%0x, %0x, %0x, %0x\n", b[1], b[5], b[9], b[13]);
  printf("%0x, %0x, %0x, %0x\n", b[2], b[6], b[10], b[14]);
  printf("%0x, %0x, %0x, %0x\n", b[3], b[7], b[11], b[15]);
  printf("\n");
}

int main(void) {
 
  uint8_t data[16] = {
    0x01, 0x02, 0x03, 0x04,
    0x05, 0x06, 0x07, 0x08,
    0x09, 0x10, 0x11, 0x12,
    0x13, 0x14, 0x15, 0x16
  };

  const unsigned char key[] = { 
    0x30, 0x31, 0x32, 0x33,
    0x34, 0x35, 0x36, 0x37,
    0x38, 0x39, 0x30, 0x31,
    0x32, 0x33, 0x34, 0x35
  };

  printBlock(data);
  aes128_encrypt(data,key);
  printBlock(data);
  return 0;
}
